#!/usr/bin/env node

/**
 * Bakal — Notion Database Setup Script
 *
 * Creates the 4 required databases in your Notion workspace:
 *   1. Campagnes — Résultats
 *   2. Campagnes — Diagnostics
 *   3. Campagnes — Historique Versions
 *   4. Mémoire Cross-Campagne
 *
 * Prerequisites:
 *   1. Create a Notion integration at https://www.notion.so/my-integrations
 *   2. Set NOTION_TOKEN in your .env file
 *   3. Share a parent page with your integration (... → Connections → Add)
 *   4. Optionally set NOTION_PARENT_PAGE_ID (auto-discovered if missing)
 *
 * Usage:
 *   npm run setup-notion
 */

require('dotenv').config();
const fs = require('fs');
const path = require('path');
const { Client } = require('@notionhq/client');

const ENV_PATH = path.join(__dirname, '..', '.env');

const notion = new Client({ auth: process.env.NOTION_TOKEN });

async function findParentPage() {
  // If NOTION_PARENT_PAGE_ID is set, use it
  if (process.env.NOTION_PARENT_PAGE_ID) {
    console.log(`📌 Using configured parent page: ${process.env.NOTION_PARENT_PAGE_ID}`);
    return process.env.NOTION_PARENT_PAGE_ID;
  }

  // Auto-discover: search workspace for any page the integration has access to
  console.log('🔍 No NOTION_PARENT_PAGE_ID set — searching workspace...');
  const search = await notion.search({
    filter: { property: 'object', value: 'page' },
    page_size: 20,
  });

  if (search.results.length === 0) {
    console.error('❌ No pages found in workspace.');
    console.error('   Make sure you shared a page with your integration:');
    console.error('   Go to any Notion page → ... → Connections → Add your integration');
    process.exit(1);
  }

  // Prefer a page named "Bakal", "Stanko", "Projet", or "Welcome"
  const keywords = ['bakal', 'stanko', 'projet', 'project', 'welcome', 'hub'];
  for (const page of search.results) {
    const props = page.properties || {};
    const titleProp = props.title || props.Name || Object.values(props).find(p => p.type === 'title');
    const title = (titleProp?.title?.[0]?.plain_text || '').toLowerCase();
    if (keywords.some(kw => title.includes(kw))) {
      console.log(`📌 Found matching page: "${titleProp.title[0].plain_text}" (${page.id})`);
      return page.id;
    }
  }

  // Fallback: use the first page
  const first = search.results[0];
  const props = first.properties || {};
  const titleProp = props.title || props.Name || Object.values(props).find(p => p.type === 'title');
  const title = titleProp?.title?.[0]?.plain_text || '(untitled)';
  console.log(`📌 Using first available page: "${title}" (${first.id})`);
  return first.id;
}

function appendToEnv(entries) {
  let content = fs.readFileSync(ENV_PATH, 'utf8');

  // Add Notion database section if not present
  if (!content.includes('NOTION_DB_RESULTATS')) {
    content += '\n# --- Notion Database IDs (auto-generated by setup-notion) ---\n';
  }

  for (const [key, value] of Object.entries(entries)) {
    // Replace existing line or append
    const regex = new RegExp(`^${key}=.*$`, 'm');
    if (regex.test(content)) {
      content = content.replace(regex, `${key}=${value}`);
    } else {
      content += `${key}=${value}\n`;
    }
  }

  fs.writeFileSync(ENV_PATH, content);
}

